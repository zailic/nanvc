import { mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { pathToFileURL } from "node:url";
import process from "node:process";

const ROOT_DIR = process.cwd();
const OPENAPI_INPUT_PATH = resolve(ROOT_DIR, "openapi/vault-api-openapi.json");
const OUTPUT_PATH = resolve(ROOT_DIR, "src/v2/generated/vault-openapi.ts");
const TARGETS_DIR = resolve(ROOT_DIR, "openapi/targets");
const PATCHES_DIR = resolve(ROOT_DIR, "openapi/patches");

const targetModules = await loadTargetModules();
const patchModules = await loadPatchModules();
const targetPaths = targetModules.flatMap((module) => module.targetPaths ?? []);
const targetSchemas = unique(targetModules.flatMap((module) => module.targetSchemas ?? []));
const schemaPatches = mergeObjects(
  patchModules.map((module) => module.schemaPatches ?? {}),
);
const responsePatches = mergeObjects(
  patchModules.map((module) => module.responsePatches ?? {}),
);
const spec = JSON.parse(readFileSync(OPENAPI_INPUT_PATH, "utf8"));
const componentSchemas = {
  ...pickEntries(spec.components?.schemas ?? {}, targetSchemas),
  ...schemaPatches,
};

const lines = [
  "// This file is generated by scripts/generate-v2-openapi-types.mjs.",
  `// Source: ${relativePath(OPENAPI_INPUT_PATH)}`,
  "// Do not edit manually.",
  "",
  "export interface components {",
  "    schemas: {",
  ...emitComponentSchemas(componentSchemas),
  "    };",
  "}",
  "",
  "export interface paths {",
  ...emitPaths(spec.paths ?? {}, targetPaths),
  "}",
  "",
  "export const unauthenticatedOperations = {",
  ...emitUnauthenticatedOperations(spec.paths ?? {}, targetPaths),
  "} as const;",
  "",
];

mkdirSync(dirname(OUTPUT_PATH), { recursive: true });
writeFileSync(OUTPUT_PATH, `${lines.join("\n")}`, "utf8");

function emitComponentSchemas(schemas) {
  return Object.entries(schemas)
    .sort(([left], [right]) => left.localeCompare(right))
    .flatMap(([name, schema]) => [
      `        ${name}: ${schemaToType(schema, 2)};`,
    ]);
}

function emitPaths(allPaths, targetPaths) {
  return targetPaths.flatMap(({ methods, sourcePath }) => {
    const pathSpec = allPaths[sourcePath];
    if (!pathSpec) {
      throw new Error(`Missing path in OpenAPI snapshot: ${sourcePath}`);
    }

    const linesForPath = [
      `    '${sourcePath}': {`,
      ...emitPathParameters(pathSpec.parameters),
      ...emitPathMethods(sourcePath, pathSpec, methods),
      "    };",
    ];

    return linesForPath;
  });
}

function emitUnauthenticatedOperations(allPaths, targetPaths) {
  return targetPaths.flatMap(({ methods, sourcePath }) => {
    const pathSpec = allPaths[sourcePath];
    if (!pathSpec) {
      throw new Error(`Missing path in OpenAPI snapshot: ${sourcePath}`);
    }

    const unauthenticatedMethods = methods
      .filter((method) => isUnauthenticatedOperation(pathSpec, method));
    if (unauthenticatedMethods.length === 0) {
      return [];
    }

    return [
      `    ${JSON.stringify(sourcePath)}: {`,
      ...unauthenticatedMethods.map((method) => `        ${method}: true,`),
      "    },",
    ];
  });
}

function isUnauthenticatedOperation(pathSpec, method) {
  return pathSpec["x-vault-unauthenticated"] === true
    || pathSpec[method]?.["x-vault-unauthenticated"] === true;
}

function emitPathParameters(parameters = []) {
  const pathParams = parameters.filter((parameter) => parameter.in === "path");
  if (pathParams.length === 0) {
    return [];
  }

  return [
    "        parameters: {",
    "            path: {",
    ...pathParams.map((parameter) => {
      const propertyType = schemaToType(parameter.schema, 4);
      const optionalMarker = parameter.required ? "" : "?";
      return `                ${toPropertyKey(parameter.name)}${optionalMarker}: ${propertyType};`;
    }),
    "            };",
    "        };",
  ];
}

function emitPathMethods(
  pathKey,
  pathSpec,
  targetMethods = ["delete", "get", "post", "put"],
) {
  return targetMethods
    .filter((method) => method in pathSpec)
    .flatMap((method) => {
      const operation = pathSpec[method];
      return [
        `        ${method}: {`,
        ...emitMethodParameters(operation.parameters),
        ...emitRequestBody(operation.requestBody),
        ...emitResponses(pathKey, method, operation.responses ?? {}),
        "        };",
      ];
    });
}

function emitMethodParameters(parameters = []) {
  const queryParams = parameters.filter(
    (parameter) => parameter.in === "query",
  );
  if (queryParams.length === 0) {
    return [];
  }

  return [
    "            parameters: {",
    "                query: {",
    ...queryParams.map((parameter) => {
      const propertyType = schemaToType(parameter.schema, 5);
      const optionalMarker = parameter.required ? "" : "?";
      return `                    ${toPropertyKey(parameter.name)}${optionalMarker}: ${propertyType};`;
    }),
    "                };",
    "            };",
  ];
}

function emitRequestBody(requestBody) {
  const schema = requestBody?.content?.["application/json"]?.schema;
  if (!schema) {
    return [];
  }

  return [
    "            requestBody: {",
    "                content: {",
    `                    'application/json': ${schemaToType(schema, 5)};`,
    "                };",
    "            };",
  ];
}

function emitResponses(pathKey, method, responses) {
  const responseEntries = Object.entries(responses)
    .filter(([statusCode]) => statusCode === "200" || statusCode === "204")
    .sort(([left], [right]) => Number(left) - Number(right))
    .map(([statusCode, response]) =>
      emitResponse(pathKey, method, statusCode, response),
    );

  if (responseEntries.length === 0) {
    throw new Error(
      `No supported responses found for ${method.toUpperCase()} ${pathKey}`,
    );
  }

  return ["            responses: {", ...responseEntries, "            };"];
}

function emitResponse(pathKey, method, statusCode, response) {
  const schema =
    responsePatches[pathKey]?.[method]?.[statusCode] ??
    response?.content?.["application/json"]?.schema;
  const description =
    typeof response?.description === "string" && response.description.length > 0
      ? response.description
      : statusCode === "204"
        ? "No Content"
        : "OK";

  const linesForResponse = [
    `                ${statusCode}: {`,
    `                    description: ${JSON.stringify(description)};`,
  ];

  if (schema) {
    linesForResponse.push(
      "                    content: {",
      `                        'application/json': ${schemaToType(schema, 6)};`,
      "                    };",
    );
  }

  linesForResponse.push("                };");
  return linesForResponse.join("\n");
}

function schemaToType(schema, indentLevel) {
  if (!schema) {
    return "unknown";
  }

  if (schema.$ref) {
    return refToType(schema.$ref);
  }

  if (Array.isArray(schema.enum) && schema.enum.length > 0) {
    return schema.enum.map((value) => JSON.stringify(value)).join(" | ");
  }

  if (schema.type === "array") {
    return `${schemaToType(schema.items, indentLevel)}[]`;
  }

  if (
    schema.type === "object" ||
    schema.properties ||
    schema.additionalProperties
  ) {
    if (schema.additionalProperties === true && !schema.properties) {
      return "Record<string, unknown>";
    }

    const required = new Set(schema.required ?? []);
    const properties = Object.entries(schema.properties ?? {}).map(
      ([name, propertySchema]) => {
        const optionalMarker = required.has(name) ? "" : "?";
        return `${indent(indentLevel + 1)}${toPropertyKey(name)}${optionalMarker}: ${schemaToType(propertySchema, indentLevel + 1)};`;
      },
    );

    if (schema.additionalProperties && schema.additionalProperties !== true) {
      properties.push(
        `${indent(indentLevel + 1)}[key: string]: ${schemaToType(schema.additionalProperties, indentLevel + 1)};`,
      );
    }

    if (properties.length === 0) {
      return "{ [key: string]: unknown; }";
    }

    return `{\n${properties.join("\n")}\n${indent(indentLevel)}}`;
  }

  switch (schema.type) {
    case "boolean":
      return "boolean";
    case "integer":
    case "number":
      return "number";
    case "string":
      return "string";
    default:
      return "unknown";
  }
}

function refToType(ref) {
  const refName = ref.split("/").at(-1);
  if (!refName) {
    throw new Error(`Invalid $ref value: ${ref}`);
  }

  return `components['schemas']['${refName}']`;
}

function pickEntries(object, keys) {
  return Object.fromEntries(
    keys.map((key) => {
      const value = object[key];
      if (!value) {
        throw new Error(`Missing schema in OpenAPI snapshot: ${key}`);
      }

      return [key, value];
    }),
  );
}

function toPropertyKey(name) {
  return /^[A-Za-z_][A-Za-z0-9_]*$/.test(name) ? name : JSON.stringify(name);
}

function indent(level) {
  return "    ".repeat(level);
}

function relativePath(filePath) {
  return filePath.replace(`${ROOT_DIR}/`, "");
}

async function loadTargetModules() {
  return loadModulesFromDirectory(TARGETS_DIR);
}

function unique(values) {
  return [...new Set(values)];
}

async function loadPatchModules() {
  return loadModulesFromDirectory(PATCHES_DIR);
}

async function loadModulesFromDirectory(directoryPath) {
  const fileNames = readdirSync(directoryPath)
    .filter((fileName) => fileName.endsWith(".js"))
    .sort((left, right) => left.localeCompare(right));

  return Promise.all(
    fileNames.map((fileName) =>
      import(pathToFileURL(resolve(directoryPath, fileName)).href),
    ),
  );
}

function mergeObjects(objects) {
  return Object.assign({}, ...objects);
}
